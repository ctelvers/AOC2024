bool test(const unsigned char *line) {
        bool increasing = false;
    if (line[0] < line[1])
      increasing = true;
    bool safe = true;
    for (int i = 1; line[i]; i++) {
      int difference = line[i] - line[i - 1];
      if (increasing) {
        safe = safe && difference >= 1 && difference <= 3;
      } else {
        safe = safe && difference >= -3 && difference <= -1;
      }
    }
    return safe;
}

